package com.example.schaltegger_ba_bluetoothle_bendlabs.angle;

public class AnglePair {
    private float x;
    private float y;

    public AnglePair(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }


    public float getY() {
        return y;
    }

}
